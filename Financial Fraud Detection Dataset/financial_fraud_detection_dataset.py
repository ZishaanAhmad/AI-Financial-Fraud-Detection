def get_skewwed_dataframe(df):
    pd.options.display.float_format = '{:,.4f}'.format
    sk_df = pd.DataFrame([{'column': c,
                          'uniq': df[c].nunique(),
                          'skewness': df[c].value_counts(normalize=True).values[0] * 100
                          } for c in df.columns])
    sk_df = sk_df.sort_values('skewness', ascending=False)
    return sk_df

